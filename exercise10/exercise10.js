function reduce(array, combine, start){
    let current = start;
    for(let element of array){
        current = combine(current, element);
    }
    return current
}

function characterCount(script){
    return script.ranges.reduce((count, [from, to]) => {
        return count + (to - from)
    }, 0)
}

